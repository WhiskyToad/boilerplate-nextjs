// Shared types for content script modules
// Note: Types are global across all content scripts loaded by manifest

interface ElementData {
  tagName: string;
  id: string;
  className: string;
  name: string;
  type: string;
  textContent: string;
  value: string;
  placeholder: string;
  alt: string;
  title: string;
  href: string;
  boundingRect: DOMRect;
  styles: any;
  selector: string;
  xpath: string;
  url: string;
  viewport: {
    width: number;
    height: number;
    scrollX: number;
    scrollY: number;
  };
}

interface BubbleConfig {
  text: string;
  position?: 'auto' | 'top' | 'bottom' | 'left' | 'right';
  style?: 'tooltip' | 'callout' | 'speech';
  autoGenerated?: boolean;
}

interface ContentStepData {
  type: string;
  element: ElementData | any;
  interactions?: any;
  annotations?: {
    bubble?: BubbleConfig;
  };
}

interface MessageData {
  type: string;
  data?: any;
}
